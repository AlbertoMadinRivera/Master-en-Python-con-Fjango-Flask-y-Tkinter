"""
Una función lambda es una forma rápida de crear una pequeña función y simple.
    Es como una función exprés, no necesita de un nombre (Aunque se puede asignar si se desea)
    Se usa para tareas pequeñas, cuando se necesite algo rápido y no se quiere escribir una función completa con
    def
"""

# Ejemplo 8: Funcuones lambda
print("########## Ejemplo 8 ##########")

dime_el_year = lambda year: f"El año es {year}"

print(dime_el_year(2025))

# Todo esto está en una sola línea para una tarea simple y sencilla